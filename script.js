function ensureSelectorsReady() {
  if (gradeSelect.options.length === 0) {
    const fallbackGrades = Object.keys(curriculumMap).map(Number).sort((a, b) => a - b);
    fallbackGrades.forEach((g) => {
      const option = document.createElement("option");
      option.value = String(g);
      option.textContent = `${g}. Sınıf`;
      gradeSelect.appendChild(option);
    });
  }

  gradeSelect.disabled = gradeSelect.options.length === 0;
  refreshCourses();
  courseSelect.disabled = courseSelect.options.length === 0;
}

function bootstrapApp() {
  initFilters();
  ensureSelectorsReady();
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", bootstrapApp);
} else {
  bootstrapApp();
}
