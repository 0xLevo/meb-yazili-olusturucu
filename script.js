function buildQuestionBank() {
  const rows = [];
  const enrichedMap = enrichCurriculumMap(curriculumMap);

  Object.entries(enrichedMap).forEach(([grade, courses]) => {
    Object.entries(courses).forEach(([course, terms]) => {
      Object.entries(terms).forEach(([term, exams]) => {
        Object.entries(exams).forEach(([examNo, outcomes]) => {
          outcomes.forEach((outcome) => {
            for (let variant = 0; variant < 3; variant += 1) {
              rows.push({
                grade: Number(grade),
                course,
                term: Number(term),
                examNo: Number(examNo),
                outcome,
                question: storyQuestionForCourse(course, outcome, variant),
                variant: variant + 1
              });
            }
          });
        });
      });
    });
  });

  return rows;
}

