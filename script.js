function refreshQuestionBrowser() {
  const scoped = getScopedQuestions();
  const selectedRow = kazanımlarContainer.querySelector(".kazanım-item.active");
  if (!selectedRow) {
    questionBrowserInfo.textContent = "Bir kazanıma tıklayarak o kazanımdaki soruları görüntüleyin.";
    manualQuestions.innerHTML = "";
    return;
  }

  const code = selectedRow.dataset.code;
  activeOutcomeCode = code;
  const questions = scoped.filter((q) => q.outcome.code === code);

  questionBrowserInfo.textContent = `${code} kazanımına ait ${questions.length} soru bulundu. Manuel seçebilirsiniz.`;
  manualQuestions.innerHTML = "";

  questions.forEach((q) => {
    const row = document.createElement("label");
    row.className = "manual-question-item";

    const cb = document.createElement("input");
    cb.type = "checkbox";
    cb.checked = manualSelectedQuestionIds.has(q.id);
    cb.addEventListener("change", () => {
      if (cb.checked) manualSelectedQuestionIds.add(q.id);
      else manualSelectedQuestionIds.delete(q.id);
    });

    const span = document.createElement("span");
    span.textContent = q.question;

    row.appendChild(cb);
    row.appendChild(span);
    manualQuestions.appendChild(row);
  });
}

function refreshKazanımlar() {
  const scoped = getScopedQuestions();
  const outcomes = unique(scoped.map((q) => `${q.outcome.code}|${q.outcome.text}`));

  kazanımlarContainer.innerHTML = "";
  manualSelectedQuestionIds.clear();
  activeOutcomeCode = null;

  if (outcomes.length === 0) {
    kazanımlarContainer.innerHTML = "<p>Bu filtre için soru bulunamadı.</p>";
    refreshQuestionBrowser();
    return;
  }

  outcomes.forEach((raw, idx) => {
    const [code, text] = raw.split("|");
    const row = document.createElement("label");
    row.className = "kazanım-item";
    row.dataset.code = code;

    const checkbox = document.createElement("input");
    checkbox.type = "checkbox";
    checkbox.value = code;
    checkbox.checked = idx < Math.min(6, outcomes.length);

    const labelText = document.createElement("span");
    labelText.innerHTML = `<span class="kazanım-code">${code}</span> — ${text}`;

    const countInput = document.createElement("input");
    countInput.type = "number";
    countInput.min = "0";
    countInput.max = "8";
    countInput.value = checkbox.checked ? "1" : "0";
    countInput.className = "kazanım-count";
    countInput.title = "Bu kazanımdan kaç soru istiyorsunuz?";

    checkbox.addEventListener("change", () => {
      if (!checkbox.checked) countInput.value = "0";
      else if (Number(countInput.value) === 0) countInput.value = "1";
    });

    row.addEventListener("click", (e) => {
      if (e.target === countInput || e.target === checkbox) return;
      kazanımlarContainer.querySelectorAll('.kazanım-item').forEach((el) => el.classList.remove('active'));
      row.classList.add('active');
      refreshQuestionBrowser();
    });

    row.appendChild(checkbox);
    row.appendChild(labelText);
    row.appendChild(countInput);
    kazanımlarContainer.appendChild(row);
  });

  const first = kazanımlarContainer.querySelector('.kazanım-item');
  if (first) {
    first.classList.add('active');
    refreshQuestionBrowser();
  }
}

function getOutcomeSelections() {
  const rows = [...kazanımlarContainer.querySelectorAll('.kazanım-item')];
  return rows
    .map((row) => {
      const cb = row.querySelector('input[type="checkbox"]');
      const count = Number(row.querySelector('.kazanım-count').value || 0);
      return { code: cb.value, checked: cb.checked, count: Math.max(0, Math.min(8, count)) };
    })
    .filter((item) => item.checked && item.count > 0);
}

function getVisualForQuestion(questionText) {
  const lower = questionText.toLowerCase();
  if (lower.includes('üçgen') || lower.includes('çember') || lower.includes('geometr')) {
    return '<div aria-hidden="true"><svg width="170" height="90" viewBox="0 0 170 90"><circle cx="45" cy="45" r="28" fill="none" stroke="#111" stroke-width="2"/><polygon points="110,70 150,70 130,25" fill="none" stroke="#111" stroke-width="2"/></svg></div>';
  }
  if (lower.includes('grafik') || lower.includes('veri')) {
    return '<div aria-hidden="true"><svg width="170" height="90" viewBox="0 0 170 90"><line x1="20" y1="75" x2="155" y2="75" stroke="#111" stroke-width="2"/><line x1="20" y1="75" x2="20" y2="12" stroke="#111" stroke-width="2"/><rect x="35" y="48" width="16" height="27" fill="none" stroke="#111"/><rect x="62" y="35" width="16" height="40" fill="none" stroke="#111"/><rect x="89" y="25" width="16" height="50" fill="none" stroke="#111"/></svg></div>';
  }
  return '';
}

function generateExam() {
  const grade = Number(gradeSelect.value);
  const course = courseSelect.value;
  const term = Number(termSelect.value);
  const examNo = Number(examNoSelect.value);
  const outcomeSelections = getOutcomeSelections();
  const questionCount = Number(questionCountInput.value);
  const schoolName = schoolNameInput.value.trim() || "................................";
  const teacherName = teacherNameInput.value.trim() || "................................";

  if (outcomeSelections.length === 0) {
    alert("Lütfen en az bir kazanım seçip soru adedi belirtiniz.");
    return;
  }

  const scoped = getScopedQuestions();
  const selectedQuestions = [];

  outcomeSelections.forEach((selection) => {
    const pool = scoped.filter((q) => q.outcome.code === selection.code);
    const manual = pool.filter((q) => manualSelectedQuestionIds.has(q.id));
    const autoPool = pool.filter((q) => !manualSelectedQuestionIds.has(q.id));

    const takeManual = manual.slice(0, selection.count);
    const needed = Math.max(0, selection.count - takeManual.length);
    const takeAuto = shuffle(autoPool).slice(0, needed);

    selectedQuestions.push(...takeManual, ...takeAuto);
  });

  const desiredCount = Math.max(1, Math.min(questionCount, 30));
  let finalQuestions = selectedQuestions;

  if (finalQuestions.length > desiredCount) {
    finalQuestions = finalQuestions.slice(0, desiredCount);
  } else if (finalQuestions.length < desiredCount) {
    const selectedCodes = outcomeSelections.map((s) => s.code);
    const extraPool = scoped.filter(
      (q) => selectedCodes.includes(q.outcome.code) && !finalQuestions.some((f) => f.id === q.id)
    );
    finalQuestions = [...finalQuestions, ...shuffle(extraPool).slice(0, desiredCount - finalQuestions.length)];
  }

  if (finalQuestions.length === 0) {
    alert("Seçilen kazanımlara göre soru üretilemedi.");
    return;
  }

  const today = new Date().toLocaleDateString("tr-TR");
  const yearLabel = "2025-2026 Eğitim Öğretim Yılı";
  const selectedCodes = outcomeSelections.map((s) => s.code);

  examPaper.innerHTML = `
    <h2 class="exam-title">${yearLabel} ${schoolName} Ortaokulu ${course} Dersi ${term}. Dönem ${examNo}. Yazılı Sınavı</h2>
    <div class="meta-grid">
      <div><strong>Tarih:</strong> ${today}</div>
      <div><strong>Süre:</strong> 40 Dakika</div>
      <div><strong>Ad Soyad:</strong> ____________________</div>
      <div><strong>Sınıf:</strong> ${grade}. Sınıf</div>
    </div>
    <p><strong>Kapsanan Kazanımlar:</strong> ${selectedCodes.join(", ")}</p>
    <p><strong>Soru Havuzu:</strong> ${scoped.length} soru (seçilen ${finalQuestions.length})</p>
    <ol class="questions">
      ${finalQuestions
        .map(
          (q) => `<li><strong>[${q.outcome.code}]</strong> ${q.question}${getVisualForQuestion(q.question)}<div class="answer-space"></div><div class="answer-space"></div></li>`
        )
        .join("")}
    </ol>
    <p><strong>Notlandırma:</strong> Her soru eşit puandır.</p>
    <p><strong>Öğretmen:</strong> ${teacherName}</p>
  `;

  emptyState.classList.add("hidden");
  examPaper.classList.remove("hidden");
  actions.classList.remove("hidden");
}

function downloadExamAsText() {
  if (examPaper.classList.contains("hidden")) return;
  const blob = new Blob([examPaper.innerText], { type: "text/plain;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = "yazili.txt";
  a.click();
  URL.revokeObjectURL(url);
}

function downloadExamAsDoc() {
  if (examPaper.classList.contains("hidden")) return;
  const html = `<!doctype html><html><head><meta charset="utf-8"></head><body>${examPaper.innerHTML}</body></html>`;
  const blob = new Blob([html], { type: "application/msword" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = "yazili.doc";
  a.click();
  URL.revokeObjectURL(url);
}

function downloadExamAsPdf() {
  if (examPaper.classList.contains("hidden")) return;
  alert("PDF için açılan yazdırma ekranında Hedef: PDF olarak kaydet seçebilirsiniz.");
  window.print();
}

function toggleAllOutcomes() {
  const rows = [...kazanımlarContainer.querySelectorAll('.kazanım-item')];
  if (rows.length === 0) return;

  const allChecked = rows.every((row) => row.querySelector('input[type="checkbox"]').checked);
  rows.forEach((row) => {
    const cb = row.querySelector('input[type="checkbox"]');
    const count = row.querySelector('.kazanım-count');
    cb.checked = !allChecked;
    count.value = cb.checked ? (Number(count.value) > 0 ? count.value : "1") : "0";
  });
}

