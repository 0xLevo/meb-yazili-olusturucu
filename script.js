function enrichCurriculumMap(baseMap) {
  const clone = JSON.parse(JSON.stringify(baseMap));

  Object.entries(clone).forEach(([grade, courses]) => {
    Object.entries(courses).forEach(([course, terms]) => {
      const supplements = [
        ...(supplementalOutcomeBank[Number(grade)]?.[course] || []),
        ...(additionalOutcomeBank[Number(grade)]?.[course] || [])
      ];

      Object.entries(terms).forEach(([, exams]) => {
        Object.entries(exams).forEach(([, outcomes]) => {
          supplements.forEach((supp) => {
            if (!outcomes.some((o) => o.code === supp.code)) {
              outcomes.push(supp);
            }
          });
        });
      });
    });
  });

  return clone;
}

